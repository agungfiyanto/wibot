import 'package:flutter/material.dart';

class Warna {
  static Color kuning(double opacity) {
    return Color.fromRGBO(250, 234, 72, opacity);
  }

  static Color biruKehijauan(double opacity) {
    return Color.fromRGBO(27, 87, 95, opacity);
  }

  static Color merah(double opacity) {
    return Color.fromRGBO(245, 80, 80, opacity);
  }

  static Color putih(double opacity) {
    return Color.fromRGBO(255, 255, 255, opacity);
  }

  static Color mint(double opacity) {
    return Color.fromRGBO(188, 234, 213, opacity);
  }
}
